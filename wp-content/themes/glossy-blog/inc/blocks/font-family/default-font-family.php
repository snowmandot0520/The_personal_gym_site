<?php

/* Default Font Family */
function glossy_blog_get_default_site_identity_font_family() {    
    return esc_html__( "Sora", 'glossy-blog' );
}

function glossy_blog_get_default_main_font_family() {    
    return esc_html__( "Manjari", 'glossy-blog' );
}

function glossy_blog_get_default_secondary_font_family() {    
    return esc_html__( "Manrope", 'glossy-blog' );
}